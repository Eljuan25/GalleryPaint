    <h1>¿Cómo aprender a dibujar en acuarela?</h1>

    <section class="pasos container">
        <div class="paso">
            <h3>1. Material básico</h3>
            <p>
                Para comenzar con acuarelas necesitas pinceles de diferentes tamaños, papel especial para acuarela, pinturas en pastillas o tubos, agua, una paleta para mezclar colores y papel absorbente o toalla para controlar la cantidad de agua.
            </p>
            <img src="img/accesorios_acuarela.jpg" alt="Materiales de acuarela" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>2. Ejercicios de color y trazo</h3>
            <p>
                Antes de dibujar, practica cómo se comporta la acuarela: traza líneas y curvas con distintas cantidades de agua, haz degradados y mezcla de colores, y observa cómo se difuminan los bordes.
            </p>
            <img src="img/trazos_acuarela.jpg" alt="Ejercicios de acuarela" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>3. Formas básicas y color</h3>
            <p>
                Empieza con formas simples como círculos, cuadrados o triángulos. Pinta cada forma usando degradados y mezclas para entender cómo controlar el color y la cantidad de agua.
            </p>
            <img src="img/formas_acuarela.jpg" alt="Formas básicas en acuarela" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>4. Mezclas y efectos</h3>
            <p>
                Experimenta con técnicas como húmedo sobre húmedo para transiciones suaves, húmedo sobre seco para bordes definidos, y usa sal, esponja o papel film para crear texturas interesantes.
            </p>
            <img src="img/efectos_acuarela.jpg" alt="Efectos de acuarela" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>5. Observación</h3>
            <p>
                Observa colores, luces y sombras de objetos reales. Practica pintando frutas, hojas, flores o paisajes usando acuarela para mejorar tu percepción del color y la luz.
            </p>
            <img src="img/observacion_acuarela.jpg" alt="Ejercicio de observación con acuarela" style="width: 350px;">
        </div>
    </section>

    <section class="container ejemplo" style="display: flex; align-items: flex-start; gap: 20px;">
        <div style="flex: 1;">
            <h2>Ejemplo práctico: Pintando un árbol en acuarela</h2>
            <p>Para aplicar lo aprendido, pintemos un árbol paso a paso:</p>
            <ul>
                <li>Dibuja suavemente el contorno del árbol con lápiz.</li>
                <li>Pinta la copa del árbol con un color base claro.</li>
                <li>Añade hojas y ramas usando degradados y mezcla de colores.</li>
                <li>Pinta el tronco con marrones y sombras más oscuras para dar volumen.</li>
                <li>Ajusta detalles y luces con un pincel limpio o un poco de agua.</li>
            </ul>
        </div>
