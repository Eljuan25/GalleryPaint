    <h2>¿Cómo aprender a dibujar a lápiz?</h2>

    <section class="pasos container">
        <div class="paso">
            <h3>1. Material básico</h3>
            <p>
                Antes de comenzar, necesitas muy pocas cosas: un lápiz, una hoja de papel y una goma de borrar.
                Lo ideal es contar con lápices de diferente dureza, por ejemplo:
                <br><b>HB</b> para trazos generales, 
                <b>2B</b> para sombras suaves y 
                <b>4B o 6B</b> para sombras oscuras.
                <br><br>
                Una libreta de bocetos es excelente para practicar todos los días sin preocuparte por el resultado.
            </p>
            <img src="img/lapices.jpg" alt="Lápices de dibujo básicos" style="width: 350px;" >
        </div>

        <div class="paso">
            <h3>2. Trazos simples</h3>
            <p>
                El primer paso para aprender a dibujar es <b>soltar la mano</b>. 
                Practica haciendo líneas rectas, curvas, círculos, espirales y óvalos.
                No busques que salgan perfectos, lo importante es ganar confianza y mejorar el control del lápiz.
                <br><br>
                Un ejercicio útil es llenar una página completa solo con líneas rectas o círculos, 
                variando la presión para que unas se vean claras y otras más oscuras.
            </p>
            <img src="img/trazos_basicos.jpg" alt="Ejercicios de trazos básicos" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>3. Figuras geométricas</h3>
            <p>
                Todas las cosas que ves a tu alrededor se pueden reducir a figuras básicas: 
                <b>cubos, esferas, cilindros y conos</b>. 
                Aprender a dibujar estas formas te ayudará a construir objetos más complejos después.
                <br><br>
                Imagina que una taza es un cilindro, una pelota es una esfera y una caja es un cubo. 
                Practicar estas figuras con diferentes ángulos hará que luego te sea mucho más fácil dibujar del natural.
            </p>
            <img src="img/figuras.jpg" alt="Ejemplo de figuras geométricas" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>4. Sombras y volumen</h3>
            <p>
                El siguiente paso es aprender a dar <b>profundidad</b>. 
                Para ello, observa cómo la luz afecta a los objetos. 
                El área más cercana a la luz será clara, y el lado opuesto tendrá sombra.
                <br><br>
                Usa diferentes presiones con el lápiz: 
                <b>suave</b> para zonas iluminadas y <b>fuerte</b> para sombras intensas. 
                También puedes usar tu dedo o un difumino para suavizar los bordes de las sombras.
            </p>
            <img src="img/sombras.jpg" alt="Ejemplo de sombreado a lápiz" style="width: 350px;">
        </div>

        <div class="paso">
            <h3>5. Observación</h3>
            <p>
                El dibujo no solo depende de tu mano, sino de tu <b>ojo</b>. 
                Dedica tiempo a observar objetos reales: frutas, tazas, plantas o cualquier cosa sencilla.
                <br><br>
                Fíjate en los detalles: la forma, la proporción, las luces y sombras. 
                Cuanto más practiques la observación, mejor podrás representarlo en papel.
            </p>
            <img src="img/observaciones.jpg" alt="Ejercicio de observación con objetos" style="width: 350px;">
        </div>
    </section>

