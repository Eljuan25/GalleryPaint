    <div style="flex: 1;">
        <h2>Ejemplo práctico: Dibujo de una manzana</h2>
        <p>Para aplicar lo aprendido, dibujemos una manzana paso a paso:</p>
        <ul>
            <li>1. Dibuja un círculo como base, sin preocuparte porque quede perfecto.</li>
            <li>2. Añade el tallo y un pequeño hundimiento en la parte superior.</li>
            <li>3. Traza suavemente las curvas que le dan forma.</li>
            <li>4. Observa la luz: elige de dónde viene y sombrea el lado contrario.</li>
            <li>5. Difumina para lograr un efecto suave y realista.</li>
        </ul>
    </div>
